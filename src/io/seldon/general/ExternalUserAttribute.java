package io.seldon.general;

import java.io.Serializable;

/**
 * @author philipince
 *         Date: 17/03/2014
 *         Time: 12:28
 */
public class ExternalUserAttribute implements Serializable{

    private String id;
    public String uid;
    public String type;
    public String value;

    public ExternalUserAttribute(String uid, String type, String value) {
        this.uid = uid;
        this.type = type;
        this.value = value;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
